#ifndef __DS1302_COMPENSATION_H
#define __DS1302_COMPENSATION_H

#include "sys.h"

/*══════════════════════════════════════════════════════════════
 *  DS1302 晶振偏差软件补偿
 *
 *  原理：DS1302 走时偏差来源于 32.768kHz 晶振频率误差。
 *  该误差是恒定的（同一温度下每天偏固定的秒数）。
 *  本模块每隔 CORRECTION_INTERVAL_H 小时自动修正一次。
 *
 *  使用步骤：
 *    1. 不设补偿，跑一周，记录与实际时间的偏差（秒）
 *    2. 换算：CORRECTION_SECONDS_PER_DAY = 总偏差 / 天数
 *       （正数 = DS1302 偏快，需减去；负数 = 偏慢，需加上）
 *    3. 修改下方宏，重新编译烧录
 *    4. DS1302 RAM 0xC0~0xC2 自动记录修正状态，断电不丢
 *══════════════════════════════════════════════════════════════*/

/*──────────────────────────────────────────────────────────────
 *  用户配置区 — 实测后修改这两个值
 *──────────────────────────────────────────────────────────────*/

/* 每天偏差秒数。正数 = DS1302 偏快（需回拨），负数 = 偏慢（需前拨）
 * 例：7天快了45秒 → 45/7≈6.4 → 填 6.4 */
#define CORRECTION_SECONDS_PER_DAY   2.0f    /* 实测: ~1分钟/月 → 约2秒/天 (DS1302偏快) */

/* 修正间隔（小时）。24 = 每天凌晨修正一次，推荐 24 */
#define CORRECTION_INTERVAL_H        24

/* 每天在第几小时触发修正（0-23）。凌晨 3:00 修正避免与显示冲突 */
#define CORRECTION_TRIGGER_HOUR      3


/*──────────────────────────────────────────────────────────────
 *  DS1302 RAM 地址分配（电池后备，断电不丢失）
 *──────────────────────────────────────────────────────────────*/

#define DS1302_RAM_CORR_DAY          0xC0    /* 上次修正日 (BCD: 0x01-0x31) */
#define DS1302_RAM_CORR_HOUR         0xC1    /* 上次修正时 (BCD: 0x00-0x23) */
#define DS1302_RAM_CORR_REMAINDER    0xC2    /* 亚秒余数 (0-99, 百分秒) */


/*──────────────────────────────────────────────────────────────
 *  API
 *──────────────────────────────────────────────────────────────*/

void ds1302_compensation_init(void);
void ds1302_compensation_tick(void);

/* BCD ↔ 十进制 转换（供补偿模块内部用） */
uint8_t bcd_to_dec(uint8_t bcd);
uint8_t dec_to_bcd(uint8_t dec);

#endif
